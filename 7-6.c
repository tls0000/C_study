#include <stdio.h>

void main()
{
	int menu;

	do {
		printf("\n손님 주문하시겠습니까 ?.\n");
		printf("<1>카페라떼 <2>카푸치노 <3>아메리카노 <4>그만시킬래요 ==>");
		scanf("%d",&menu);

		switch(menu)
		{
                        case 1 : printf("#카페라떼 주문하셨습니다."); break;
                        case 2 : printf("#카푸치노 주문하셨습니다."); break;
                        case 3 : printf("#아메리카노 주문하셨습니다."); break;
                        case 4 : printf("#주문하신 커피 주문하셨습니다."); break;
			default : printf("잘못 주문하셨습니다.\n");
		}
	}while (menu !=4);
}

